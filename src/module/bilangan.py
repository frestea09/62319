def tambah(bilanganPertama, bilanganKedua ):
    hasil = bilanganPertama + bilanganKedua
    return hasil
def kurang(bilanganPertama, bilanganKedua):
    hasil = bilanganPertama - bilanganKedua
    return hasil
def bagi(bilanganPertama, bilanganKedua):
    hasil = bilanganPertama/bilanganKedua
    return hasil
def kali(bilanganPertama, bilanganKedua):
    hasil = bilanganPertama*bilanganKedua
    return hasil
def kuadrat(bilanganPertama, bilanganKedua):
    hasil = bilanganPertama ** bilanganKedua
    return hasil 